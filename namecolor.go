package main

import "image/color"

var nameBackColors = []color.RGBA{
	{0xff, 0xff, 0xff, 0xff}, // white
	{0x60, 0xff, 0x60, 0xff}, // green
	{0xff, 0xff, 0x60, 0xff}, // yellow
	{0xff, 0x60, 0x60, 0xff}, // red
	{0x00, 0x00, 0x00, 0xff}, // black
	{0x90, 0xff, 0xff, 0xff}, // blue
	{0x80, 0x80, 0x80, 0xff}, // grey
	{0x00, 0x80, 0x80, 0xff}, // cyan
	{0x99, 0x66, 0x00, 0xff}, // orange
}

var nameTextColors = []color.RGBA{
	{0x00, 0x00, 0x00, 0xff}, // black
	{0xff, 0xff, 0xff, 0xff}, // white
	{0xff, 0x00, 0x00, 0xff}, // red
	{0x00, 0x00, 0xc0, 0xff}, // blue
}

func mobileNameColors(code uint8) (text, bg, frame color.RGBA) {
	b := int((code >> 4) & 0x0f)
	if b >= len(nameBackColors) {
		b = 0
	}
	t := int((code >> 2) & 0x03)
	if t >= len(nameTextColors) {
		t = 0
	}
	return nameTextColors[t], nameBackColors[b], color.RGBA{0, 0, 0, 0xff}
}
