// Bard instruments -- must match class CCLInstrument
type 'clIn'
{
	wide array Instruments
		{
			// First 76 bytes must exactly conform to the layout of QTMA's
			// struct ToneDescription{} -- cf. <QuickTime/QuickTimeMusic.h>
		literal longint;		// synthesizer type
		pstring[31];			// name of instantiation of synthesizer
		pstring[31];			// preferred name for human use
MIDINum:
		longint;				// inst. number used if synth-name matches
								// best matching General MIDI number
								// (same as MIDINum)
		longint = $$Long( MIDINum[ $$ArrayIndex( Instruments ) ] );
			// end embedded ToneDescription
		
			// our remaining custom fields
		integer;				// octave offset
		fill bit[4];			// mUnused
		bitstring[4];			// polyphony
			// 8  bits of flags
		fill bit[5];					// (I skipped the 'custom' flag)
		boolean	plain, longChord;		// flags_LongChord
		bitstring[2]					// noMelody & noChord flags, conflated
			std = 0, melodyOnly = 1, chordsOnly = 2;	// (3 is nonsensical)
		
		integer full = 100;		// chord velocity, in percents
		integer full = 100;		// melody velocity, in percents
		unsigned hex longint;	// restrictions 0-31
		unsigned hex longint;	// restrictions 32-35, filler 36-63
		pstring[31];			// CL name (not actually used)
		};
};


